// Challenge 2:  Create the function for the Average program.  Refer to the resource link in the HTML for assistance.
function calculateAverage(){
    let g1 = document.getElementById("g1").value;
    let g2 = document.getElementById("g2").value;
    let g3 = document.getElementById("g3").value;
    let op = document.getElementById("output");
    let average = (parseFloat(g1) + parseFloat(g2) + parseFloat(g3)) / 3;
    op.innerHTML = "Average: " + average;
}

